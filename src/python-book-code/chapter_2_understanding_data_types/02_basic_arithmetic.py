# Basic arithmetic
a = 10 + 5      # Addition: 15
b = 10 - 3      # Subtraction: 7
c = 4 * 6       # Multiplication: 24
d = 20 / 4      # Division: 5.0 (always returns float!)

# Special operations
e = 20 // 3     # Floor division: 6 (rounds down)
f = 20 % 3      # Modulo (remainder): 2
g = 2 ** 3      # Exponentiation: 8 (2 to the power of 3)

# Operator precedence (PEMDAS)
result = 2 + 3 * 4      # 14 (not 20!)
result = (2 + 3) * 4    # 20 (use parentheses for clarity)

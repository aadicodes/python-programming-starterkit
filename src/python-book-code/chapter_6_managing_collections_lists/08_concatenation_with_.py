# Concatenation with +
technical_books = ["Python", "Java"]
business_books = ["Marketing", "Finance"]
all_books = technical_books + business_books
print(all_books)  # ['Python', 'Java', 'Marketing', 'Finance']

# Repetition with *
promo = ["Special Offer!"] * 3
print(promo)  # ['Special Offer!', 'Special Offer!', 'Special Offer!']
